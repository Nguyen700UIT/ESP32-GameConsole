void initBait()
{
    initBaitAt(bait, randomNormalBaitType(), specialBait.active ? &specialBait : nullptr);
}

void updateSpecialBait()
{
    if (!specialBait.active)
    {
        return;
    }

    if (currTime - specialBait.spawnedAt >= SPECIAL_BAIT_LIFETIME)
    {
        clearBait(specialBait);
        specialBait.active = false;
    }
}

// Reset snake to normal state
void resetSnake() {
    headX = 60;
    headY = 30;
    nTail = 1;
    tailX[0] = headX;
    tailY[0] = headY;
}

void readDirection()
{
    // Đọc cờ ngắt và chặn lỗi quay đầu 180 độ
    if (isUp && snakeDir != down) {
        snakeDir = up;
    }
    else if (isDown && snakeDir != up) {
        snakeDir = down;
    }
    else if (isLeft && snakeDir != right) {
        snakeDir = left;
    }
    else if (isRight && snakeDir != left) {
        snakeDir = right;
    }

    isUp = false;
    isDown = false;
    isLeft = false;
    isRight = false;
}

void move()
{
    prevTailX = tailX[nTail-1];
    prevTailY = tailY[nTail-1];
    int prevX = tailX[0];
    int prevY = tailY[0];
    tailX[0] = headX;
    tailY[0] = headY;
    int tempX, tempY;
    for (int i = 1; i < nTail; ++i)
    {
        tempX = tailX[i];
        tempY = tailY[i];
        tailX[i] = prevX;
        tailY[i] = prevY;
        prevX = tempX;
        prevY = tempY;
    }
    switch(snakeDir)
    {
        case up:
            headY-=SNAKE_BLOCK_SIZE;
            break;
        case down:
            headY+=SNAKE_BLOCK_SIZE;
            break;
        case left:
            headX-=SNAKE_BLOCK_SIZE;
            break;
        case right:
            headX+=SNAKE_BLOCK_SIZE;
            break;
    }
    // if (headX < 0) headX = SNAKE_BOARD_WIDTH - SNAKE_BLOCK_SIZE; else if (headX >= SNAKE_BOARD_WIDTH) headX = 0;
    // if (headY < 0) headY = SNAKE_BOARD_HEIGHT - SNAKE_BLOCK_SIZE; else if (headY >= SNAKE_BOARD_HEIGHT) headY = 0;

}

void isGameOver()
{
    if(headX < 0 || headX >= SNAKE_BOARD_WIDTH || headY < 0 || headY >= SNAKE_BOARD_HEIGHT)
       gameOver = true;
    for (int i = 0; i < nTail - 1; ++i)
    {
        if (headX == tailX[i] && headY == tailY[i]) 
        {
            gameOver = true;
            break;
        }
    }
}

bool eat()
{
    if (bait.active && headX == bait.x && headY == bait.y) 
    { 
        clearBait(bait);
        applyBaitEffect(bait.type);
        normalBaitEaten++;
        initBait();

        if (normalBaitEaten % 5 == 0)
        {
            spawnSpecialBait();
        }

        queueSend(SOUND_EAT);
        return true;
    }

    if (specialBait.active && headX == specialBait.x && headY == specialBait.y)
    {
        clearBait(specialBait);
        applyBaitEffect(specialBait.type);
        specialBait.active = false;
        queueSend(SOUND_EAT);
        return true;
    }

    return false;
}

void drawSnake(snakeColor color)
{
    
    tft_snake.fillRect(prevTailX, prevTailY, SNAKE_BLOCK_SIZE, SNAKE_BLOCK_SIZE, GAME_GREEN);
    const uint16_t* currentHeadBmp = headBlueRight; // Mặc định nếu mới vào game
    const uint16_t* snakeBody = snakeBlueBody;
    // 1. CHỌN MẢNG ĐẦU RẮN THEO HƯỚNG
    switch(color)
    {
        case BLUE:
            if (snakeDir == up) currentHeadBmp = headBlueUp;
            else if (snakeDir == down) currentHeadBmp = headBlueDown;
            else if (snakeDir == left) currentHeadBmp = headBlueLeft;
            else if (snakeDir == right) currentHeadBmp = headBlueRight;
            snakeBody = snakeBlueBody;
            break;
        case RED:
            if (snakeDir == up) currentHeadBmp = headRedUp;
            else if (snakeDir == down) currentHeadBmp = headRedDown;
            else if (snakeDir == left) currentHeadBmp = headRedLeft;
            else if (snakeDir == right) currentHeadBmp = headRedRight;
            snakeBody = snakeRedBody;
            break;
        case YELLOW:
            if (snakeDir == up) currentHeadBmp = headYellowUp;
            else if (snakeDir == down) currentHeadBmp = headYellowDown;
            else if (snakeDir == left) currentHeadBmp = headYellowLeft;
            else if (snakeDir == right) currentHeadBmp = headYellowRight;
            snakeBody = snakeYellowBody;
            break;
        default:
            break;
    }

    

    tft_snake.setSwapBytes(true);

    // 4. Vẽ thân rắn (Dùng pushImage cho mảng màu)
    // Cú pháp: pushImage(x, y, w, h, mảng);
    tft_snake.pushImage(tailX[0], tailY[0], SNAKE_BLOCK_SIZE, SNAKE_BLOCK_SIZE, (const uint16_t*)snakeBody);

    // 5. Vẽ đầu rắn
    tft_snake.pushImage(headX, headY, SNAKE_BLOCK_SIZE, SNAKE_BLOCK_SIZE, (const uint16_t*)currentHeadBmp);

    tft_snake.setSwapBytes(false); // Tắt sau khi vẽ xong
}


