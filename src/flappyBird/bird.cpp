int birdFrame = 0;
unsigned int lastFrameTime = 0;
float birdY = 100;
int birdWidth = 17;
int birdHeight = 17;
float birdVelocity = 0;
const float GRAVITY = 0.4;
float jumpForce = -4.0;

void updateAnimation()
{
  if (millis() - lastFrameTime > 120) {
    birdFrame++;
    if (birdFrame > 2) birdFrame = 0;
    lastFrameTime = millis();
  }
}

void drawBird()
{
    const uint16_t* frames[] = {birdUP_565, birdMID_565, birdDOWN_565};
    canvas.setSwapBytes(true);
    canvas.pushImage(BIRD_X, (int)birdY, birdWidth, birdHeight, frames[birdFrame]);
    canvas.setSwapBytes(false);
}

void birdDropLogic()
{
  birdY += birdVelocity;
  birdVelocity += GRAVITY;
}

void birdJumpLogic()
{
  birdVelocity = jumpForce;
  queueSend(SOUND_JUMP);
}

void resetBird()
{
  birdY = 100;
  birdVelocity = 0;
}

